spin = input()
charge = input()

if spin == "1" and charge == "0":
    print("Photon Boson")

if spin == "1/2" and charge == "0":
    print("Neutrino Lepton")

if spin == "1/2" and charge == "-1":
    print("Electron Lepton")

if spin == "1/2" and charge == "2/3":
    print("Charm Quark")

if spin == "1/2" and charge == "-1/3":
    print("Strange Quark")

